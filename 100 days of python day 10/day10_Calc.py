def add(n1,n2):
    return n1+n2
def subtract(n1,n2):
    return n1-n2
def multiply(n1,n2):
    return n1*n2
def divide(n1,n2):
    return n1/n2
cal={'+':add,'-':subtract,'*':multiply,'/':divide}
def calculator():
    print(logo)
    num1=float(input("What\'s the first number: "))
    for symbol in cal:
        print(symbol)
    type='y'
    while type=='y':
        pick_symbol=input("pick an operation: ")
        num2=float(input("What\'s the next number: "))
        calfunc=cal[pick_symbol]
        answer=calfunc(num1,num2)
        print(f"{num1}{pick_symbol}{num2}={answer}")
        type=input(f"Type 'y' to continue calculation with {answer}, or type 'n' to exit:  ")
        if type=='y':
            num1=answer
        else:
            calculator()
calculator()